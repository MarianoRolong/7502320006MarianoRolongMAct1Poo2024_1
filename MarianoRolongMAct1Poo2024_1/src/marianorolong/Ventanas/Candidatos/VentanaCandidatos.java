    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
      
        //RECUPERAR EL CODIGO DEL FORMULARIO
    String codigo = campoCodigo.getText();
    
    //verificamos si el diccionario existe o si esta vacio
    if(Candidato.candidatosBD == null || Candidato.candidatosBD.isEmpty()){
    String msj = " No existe candidato en la BD";
    JOptionPane.showMessageDialog(this, msj);
    }
    
    else{
        //buscar el usuario en el diccionario a partir de la identificacion
    if(Candidato.candidatosBD.containsKey(codigo)){
        this.candidato = Candidato.candidatosBD.get(codigo);
        
        campoAltura.setText(this.candidato.altura);
        campoCodigo.setText(this.candidato.codigo);
        campoColorOjos.setText(this.candidato.colorOjos);
        campoColorPelo.setText(this.candidato.colorPelo);
        campoEspecialidad.setText(this.candidato.especialidad);
        campoFoto.setText(this.candidato.fotografia);
        campoNacimiento.setDate(this.candidato.fechaNacimiento);
        campoProvincia.setText(this.candidato.provincia);
        campoSexo.setText(this.candidato.sexo);
        campoExperiencia.setText(this.candidato.experiencia);
   
        botonEditar.setEnabled(true);
        botonEliminar.setEnabled(true);
    }
    else{
     String msj = " El candidato con codigo"+ codigo + "no existe";
    JOptionPane.showMessageDialog(this, msj);
    }
    }

        // TODO add your handling code here:
        
        
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        
        //botonBuscarActionPerformed(evt);
        //validamos que el campo identificacion tenga datos
        if(campoCodigo.getText() == null || campoCodigo.getText().isEmpty()){
        String msj = "Para editar primero debe tener un candidato " ;
        JOptionPane.showMessageDialog(this, msj);
        limpiarCampos();
        return;
        }
        
        if(campoCodigo.getText().equals(this.candidato.codigo) != true){
           String msj = "La identificacion no coincide con la identificacion del candidato consultado" ;
        JOptionPane.showMessageDialog(this, msj);
        limpiarCampos(); 
        return;
        }
        
        
        //obtenemos el usuario desde el diccionario a partir del codigo
        this.candidato = Candidato.candidatosBD.get(campoCodigo.getText());
        //obtenemos los nuevos valores ingresados
        
        String altura = campoAltura.getText();
        String codigo = campoCodigo.getText();
        String colorOjos = campoColorOjos.getText();
        String colorPelo = campoColorPelo.getText();
        String especialidad = campoEspecialidad.getText();
        String fotografia = campoFoto.getText();
        String experiencia = campoExperiencia.getText();
        Date fechaNacimiento = campoNacimiento.getDate();
        String provincia = campoProvincia.getText();
        String sexo = campoSexo.getText();
        
        
        // cambiar datos del candidato 
        this.candidato.fotografia = fotografia;
        this.candidato.altura = altura;
        this.candidato.colorOjos = colorOjos;
        this.candidato.colorPelo = colorPelo;
        this.candidato.especialidad = especialidad; 
        this.candidato.codigo = String.valueOf(codigo);
        this.candidato.experiencia = experiencia;
        this.candidato.fechaNacimiento = fechaNacimiento;
        this.candidato.provincia = provincia;
        this.candidato.colorPelo = colorPelo;
        this.candidato.sexo = sexo;
        
        
        // guardamos el usuario modificado
        Candidato.candidatosBD.put(this.candidato.codigo,this.candidato);
        String msj = "Candidato modificado con exito " ;
        JOptionPane.showMessageDialog(this, msj);
        
        
        
        
    }//GEN-LAST:event_botonEditarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
         //ayudenmeee :,( haaaaaaaaaaaaa
        //validamos que el campo identificacion tenga datos
        if(campoCodigo.getText() == null || campoCodigo.getText().isEmpty()){
        String msj = "Para editar primero debe tener un candidato " ;
        JOptionPane.showMessageDialog(this, msj);
        limpiarCampos();
        return;
        }
        //validar que la identificacion en el formulario coincida
        if(campoCodigo.getText().equals(this.candidato.codigo) != true){
           String msj = "El codigo no coincide con el usuario consultado" ;
        JOptionPane.showMessageDialog(this, msj);
        limpiarCampos(); 
        return;
        }
        String msj = "¿Seguro que desea eliminar este candidato?" ;
        int respuesta = JOptionPane.showConfirmDialog(this, msj, "confirmar", 
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.YES_OPTION){
        Candidato.candidatosBD.remove(this.candidato.codigo);
        int total = Candidato.candidatosBD.size();
        String msj2 = "Candidato eliminado con exito"+ total;
        JOptionPane.showInternalMessageDialog(this, msj2);
        limpiarCampos();
        }
        
        
        
        
    }//GEN-LAST:event_botonEliminarActionPerformed

