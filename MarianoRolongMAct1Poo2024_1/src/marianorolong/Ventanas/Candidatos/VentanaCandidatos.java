    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
    
        //Recuperar los datos ingresados en los campos del formulario
        String altura = campoAltura.getText();
        String codigo = campoCodigo.getText();
        String colorOjos = campoColorOjos.getText();
        String colorPelo = campoColorPelo.getText();
        String especialidad = campoEspecialidad.getText();
        String fotografia = campoFoto.getText();
        String experiencia = campoExperiencia.getText();
        Date fechaNacimiento = campoNacimiento.getDate();
        String provincia = campoProvincia.getText();
        String sexo = campoSexo.getText();
       
        
        
        //crear variable tipo candidato
        Candidato c = new Candidato();
        c.altura = altura;
        c.codigo = codigo;
        c.colorOjos = colorOjos;
        c.colorPelo = colorPelo;
        c.especialidad = especialidad;
        c.fotografia = fotografia;
        c.experiencia = experiencia;
        c.fechaNacimiento = fechaNacimiento;
        c.provincia = provincia;
        c.sexo = sexo;
        
       
        
        //VERIFICAR SI EL DICCIONARIO EXISTE
        if(Candidato.candidatosBD == null){
            Candidato.candidatosBD = new HashMap<String, Candidato>();  
        }
        //VERIFICAR SI EL CANDIDATO ESTA GUARDADO
        if(Candidato.candidatosBD.containsKey(codigo)){
            String msj = "Existe candidato con este codigo"+codigo;
            JOptionPane.showMessageDialog(this, msj);
        }
        else{
            //GUARDAMOS CANDIDATO EN EL DICCIONARIO
        Candidato.candidatosBD.put(codigo, c);
        //OBTENEMOS EL NUMERO  DE CANDIDATOS  GUARDADOS
        int cuentaCandidatos = Candidato.candidatosBD.size();
        String msj = "Este candidato fue guardado con exito " 
                + "Existen"+cuentaCandidatos+ "candidatos";
        JOptionPane.showMessageDialog(this, msj);
        limpiarCampos();
        }
        
        
    }//GEN-LAST:event_botonGuardarActionPerformed

