    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
    
        
    String codigoCasting = campoCodigoCasting.getText();
    Date fechaContratacion = campoFechaContratacion.getDate(); 
    String nombre = campoNombreCasting.getText();
    String descripcion = campoDescripcionCasting.getText();
    String costo = campoCosto.getText();
    String tipo = campoTipoDeCasting.getText();
    String numeroPersonasRequeridas = campoNumeroDePersonas.getText();

       
        
        
        //crear variable casting
       
        Casting a = new Casting();
    a.codigoCasting = codigoCasting;
    a.fechaContratacion=fechaContratacion;
    a.nombre=nombre;
    a.descripcion=descripcion;
    a.costo =costo;
    a.tipo =tipo;
    a.numeroPersonasRequeridas = numeroPersonasRequeridas;
        
        
       
        // VERIFICAR SI EL DICCIONARIO EXISTE
if (Casting.castingsBD == null) {
    Casting.castingsBD = new HashMap<String, Casting>();
}

// VERIFICAR SI EL CASTING EST√Å GUARDADO
if (Casting.castingsBD.containsKey(codigoCasting)) {
    String msj = "Existe un casting con este c√≥digo: " + codigoCasting;
    JOptionPane.showMessageDialog(this, msj);
} else {
    // GUARDAMOS CASTING EN EL DICCIONARIO
    Casting.castingsBD.put(codigoCasting, a);
    // OBTENEMOS EL N√öMERO DE CASTINGS GUARDADOS
    int cuentaCastings = Casting.castingsBD.size();
    String msj = "Este casting fue guardado con √©xito. " 
               + "Existen " + cuentaCastings + " castings";
    JOptionPane.showMessageDialog(this, msj);
    limpiarCampos();
}

        
        
        
        
    }//GEN-LAST:event_botonGuardarActionPerformed

